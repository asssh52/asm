
;-------------------CONSTS-------------------------------------------------------
BuffSize equ 10h           ; max buffer size
;--------------------------------------------------------------------------------

section .text

global _start

;================================================================================
; Comm: main func.
;================================================================================
_start:

            call printBuff

            call exitProg
;================================================================================





;================================================================================
;Comm: prints buffer to STDOUT, symbol count printed is BuffSize.
;================================================================================
printBuff:
    mov rax, 0x01           ; write64 (rdi, rsi, rdx)
    mov rdi, 1              ; stdout
    mov rsi, Buffer         ; buffer
    mov rdx, BuffSize       ; sizeof(*buffer)
    syscall

    ret

;================================================================================





;================================================================================
;Comm: exits the programm with exit code 0.
;================================================================================
exitProg:
    mov rax, 0x3c           ; func to exit
    xor rdi, rdi            ; exit code
    syscall

    ret

;==================================================================================


section     .data

Buffer:     db "000000000000000", 0ah

